package ativ_Mod1;

public class Operadoreslogic {

	public static void main(String[] args) {
//	Operadores lógicos	
		int valor1 = 10;
		int valor2 = 20;
		
		System.out.println(valor1 == 10 && valor2< 30);
		System.out.println(valor1 == 10 || valor2< 30);
		System.out.println(valor2 != 10);
		System.out.println(valor2 <= 10);
		

	}

}
